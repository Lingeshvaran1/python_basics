# Three data types under number category int, float, complex.

# int represent a whole number without decimal, both positive and negative.
a = 123434543
b = 6778787687
c = -54556
print(a,b,c)

# float is for a decimal number too.  It can have exponential part too.
m = 5.0
n = 45.6
o = -78.95
p = 15.7E4
print(m,n,o,p)

# complex type is used when number and an imaginary part is involved. "j" is used to represent an imaginary part.
x = 5j
y = 7 + 3j
print(x)
print(y)

